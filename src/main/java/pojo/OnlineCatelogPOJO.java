package pojo;

public class OnlineCatelogPOJO {
	private String demoTent;
	private String frameBackPack;
	private String sunGlasses;
	private String paddedSocks;
	private String hikingBoot;
	
	public String getDemoTent() {
		return demoTent;
	}
	public void setDemoTent(String demoTent) {
		this.demoTent = demoTent;
	}
	public String getFrameBackPack() {
		return frameBackPack;
	}
	public void setFrameBackPack(String frameBackPack) {
		this.frameBackPack = frameBackPack;
	}
	public String getSunGlasses() {
		return sunGlasses;
	}
	public void setSunGlasses(String sunGlasses) {
		this.sunGlasses = sunGlasses;
	}
	public String getPaddedSocks() {
		return paddedSocks;
	}
	public void setPaddedSocks(String paddedSocks) {
		this.paddedSocks = paddedSocks;
	}
	public String getHikingBoot() {
		return hikingBoot;
	}
	public void setHikingBoot(String hikingBoot) {
		this.hikingBoot = hikingBoot;
	}
	
	

}
